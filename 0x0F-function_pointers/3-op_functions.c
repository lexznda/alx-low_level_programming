#include "3-calc.h"

/**
 * op_add - adds two int
 * @a: num 1
 * @b:num 2
 * Return: sum
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - substrcts two int
 * @a: num 1
 * @b: num 2
 * Return: difference
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplies two int
 * @a: num 1
 * @b: num 2
 * Return: the mul
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divides two int
 * @a: num 1
 * @b: num 2
 * Return: the division
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modulus of two int
 * @a: num 1
 * @b: num 2
 * Return: the answer
 */

int op_mod(int a, int b)
{
	return (a % b);
}
